/** notfound **/
function notfound(){
  let text = "|ω・)";
  let notfound = document.getElementById('notfound');
  let size_gen = function (percentage, text) {return (percentage / text.length) + 'vw';}
  notfound.innerText = text;
  notfound.style.fontSize = (window.screen.width <= 1280) ? size_gen(70, text) : size_gen(30,text);
}

